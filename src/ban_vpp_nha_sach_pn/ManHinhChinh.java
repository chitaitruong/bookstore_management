    private void loadHDToTable(String sql)
    {
        long tongDoanhThu=0;
        try{
            Connection conn=ConnectionUtils.getSQLServerConnection();
            PreparedStatement ps=conn.prepareStatement(sql);
            ResultSet rs=ps.executeQuery();
            Vector data=new Vector();
            Vector vctData=new Vector();
            Vector vctHeader=new Vector();
            vctHeader.add("Số HD");vctHeader.add("Ngày lập");vctHeader.add("Tổng tiền");vctHeader.add("Giảm giá");vctHeader.add("Thanh toán");vctHeader.add("ID NV lập");vctHeader.add("ID KH");
            while (rs.next())
            {
                data.add(rs.getString("SOHD"));
                data.add(new SimpleDateFormat("dd-MM-yyyy").format(rs.getDate("NGAYLAP"))+" "+rs.getTime("NGAYLAP"));
                data.add(rs.getLong("TONGTIEN"));
                tongDoanhThu+=rs.getLong("TONGTIEN")-rs.getLong("GIAMGIA");
                data.add(rs.getLong("GIAMGIA"));
                data.add(rs.getLong("TONGTIEN")-rs.getLong("GIAMGIA"));
                data.add(rs.getString("IDNHANVIEN"));
                data.add(rs.getString("IDKH"));
                vctData.add(data);
                data=new Vector();
            }
            conn.close();
            DefaultTableModel tbModel1=(DefaultTableModel)jTable2.getModel();
            tbModel1.setDataVector(vctData, vctHeader);
            this.tongDoanhThuText.setText(String.valueOf(tongDoanhThu));
        } catch (Exception e)
        {
            
            e.printStackTrace();
        }
    }
