    private void xemChiTietButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xemChiTietButtonActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRowCount()==1)
        {
            DefaultTableModel tbModel=(DefaultTableModel)jTable1.getModel();
            String maPer=(String)tbModel.getValueAt(jTable1.getSelectedRow(), 0);
            maPerText3.setText(maPer);
            titleText3.setText((String)tbModel.getValueAt(jTable1.getSelectedRow(), 1));
            descriptionText3.setText((String)tbModel.getValueAt(jTable1.getSelectedRow(), 3));
            trangThaiText3.setText((String)tbModel.getValueAt(jTable1.getSelectedRow(), 2));
            try{
                Connection conn=ConnectionUtils.getSQLServerConnection();
                String sql="SELECT UPDATEDAT,CREATEDAT FROM PERMISSION WHERE MAPER=?";
                PreparedStatement ps=conn.prepareStatement(sql);
                ps.setString(1, maPer);
                ResultSet rs=ps.executeQuery();
                if (rs.next())
                {
                    if (rs.getDate("UPDATEDAT")!=null)
                        ngayCapNhatText3.setText(String.valueOf(rs.getDate("UPDATEDAT")+" "+String.valueOf(rs.getTime("UPDATEDAT"))));
                    else 
                        ngayCapNhatText3.setText("");
                    ngayTaoText3.setText(String.valueOf(rs.getDate("CREATEDAT")+" "+String.valueOf(rs.getTime("CREATEDAT"))));
                }
                conn.close();
            } catch (Exception e){
                JOptionPane.showMessageDialog(this, "Ngày tạo và ngày cập nhật không hiện thị! Lỗi(truy vấn hoặc kết nối cơ sở dữ liệu)!");
                e.printStackTrace();
            }
            CardLayout c=(CardLayout)congViecPanel.getLayout();
            c.show(congViecPanel, "chiTiet");
        } else
        {
            if (jTable1.getRowCount()==0)
            {
                JOptionPane.showMessageDialog(null, "Danh sách quyền trống!");
            } else
            {
                JOptionPane.showMessageDialog(null, "Xin hãy chọn một quyền trong danh sách để xem chi tiết!");
            }
        }
        CardLayout c=(CardLayout)congViecPanel.getLayout();
        c.show(congViecPanel, "chiTiet");
    }//GEN-LAST:event_xemChiTietButtonActionPerformed

